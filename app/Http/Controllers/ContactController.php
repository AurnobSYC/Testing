    {
        $details = [
            'name' => $request->name,
            'email' => $request->email,
            'phone' => $request->phone,
            'message' => $request->message
        ];

        Mail::to('mahimislam70@gmail.com')->send(new ContactMail($details));

        return back()->with('message_sent','Your message has been sent successfully.');
    }

